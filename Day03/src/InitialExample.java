
public class InitialExample {

	public static void main(String args[]) {
		
		int b = 100;
		System.out.println(b);
		b += 10;
		System.out.println(b);
		b-=10;
		System.out.println(b);
		b *= 100;
		System.out.println(b);
		b/= 100;
		System.out.println(b);
		b%=100;
		System.out.println(b);
		
	}
}
