
public class Mathexample {
	
	public static void main(String[] args) {
		
		int x = 10;
		int y = 11;
		System.out.println("x+y = "+(x+y));
		System.out.println("x-y="+(x-y));
		System.out.println("x*y="+(x*y));
		System.out.println("x/y="+(x/y));
		
		System.out.println("x/10.1="+(x/10.1));
		
		System.out.println(11%10);
		// 나머지연산
		System.out.println("나의 집은 시흥대로"+(100 +52)+"길 입니다.");
		
		
	}

}
